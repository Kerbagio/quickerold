const replacementKeys = Array.from(
  new Set([...Object.keys(ar), ...Object.keys(fr)]),
).sort((first, second) => second.length - first.length);

export function hasRuntimeTranslation(source: string): boolean {
  const normalized = source.trim();
  if (!normalized) return false;
  if (ar[normalized] || fr[normalized]) return true;
  return patterns.some(({ pattern }) => {
    pattern.lastIndex = 0;
    return pattern.test(normalized);
  });
}

export function translateRuntimeText(
  source: string,
  language: RuntimeLanguage,
): string {
  const normalized = source.trim();
  if (!normalized || language === "en") return normalized;

  const exact = tables[language][normalized];
  if (exact) return exact;

  for (const translator of patterns) {
    translator.pattern.lastIndex = 0;
    const match = normalized.match(translator.pattern);
    if (match) return translator[language](match);
  }

  let translated = normalized;
  for (const key of replacementKeys) {
    if (!translated.includes(key)) continue;
    const replacement = tables[language][key];
    if (replacement) translated = translated.split(key).join(replacement);
  }
  return translated;
}
